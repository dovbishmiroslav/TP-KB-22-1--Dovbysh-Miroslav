#Види помилок в pyton

# SyntaxError – помилка синтаксису коду  
# IndentationError – неправильні відступи  
# NameError – звернення до неіснуючої змінної/імені  
# TypeError – операція з несумісними типами  
# ValueError – невірне значення при правильному типі  
# IndexError – звернення до неіснуючого індексу списку/масиву  
# KeyError – звернення до відсутнього ключа словника  
# AttributeError – звернення до неіснуючого атрибуту об’єкта  
# ImportError – помилка імпорту модуля або його частини  
# ModuleNotFoundError – модуль не знайдено  
# ZeroDivisionError – ділення на нуль  
# FileNotFoundError – спроба відкрити відсутній файл  
# PermissionError – недостатньо прав для операції (наприклад, з файлом)  
# OSError – помилка ОС (робота з файлами, шляхами тощо)  
# RuntimeError – загальна помилка під час виконання  
# RecursionError – перевищена глибина рекурсії  
# OverflowError – занадто велике число для операції  
# MemoryError – недостатньо пам’яті  
# EOFError – неочікуваний кінець вводу  
# StopIteration – ітератор завершений  
